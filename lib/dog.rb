class Dog
  attr_accessor :name, :breed
  def initialize(breed = "Mutt")
    @breed = breed
  end
end

class Dog

  def name= (name)
    @name
  end

  def name
    @name
  end

  def initialize(breed)
    @breed = breed
  end

  def breed= (breed)
    @breed
  end

  def breed
    @breed
  end

end
